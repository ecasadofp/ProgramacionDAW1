package JUnitED.ejemplo1;

public class Calculadora {

public int suma(int a, int b) {return a+b;}

public int restar(int a, int b) {return a -b;};

public int dividirEteron(int a, int b) {return a/b;}

public double dividirDecimal(int a, int b) {return (double)a/b;}

public double raizCuadrada(int a) {return Math.sqrt(a);}

}
